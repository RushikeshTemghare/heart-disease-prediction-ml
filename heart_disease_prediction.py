"""# 🏷️ One-Hot Encoding

Categorical variables are transformed into numerical features using One-Hot Encoding.

This enables machine learning algorithms to process categorical information effectively.
"""

df_encoded = pd.get_dummies(df,drop_first=True)

df_encoded

df_encoded = df_encoded.astype(int)

df_encoded

df_encoded.columns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, f1_score, classification_report
from sklearn.linear_model import LogisticRegression
from sklearn.naive_bayes import GaussianNB
from sklearn.tree import DecisionTreeClassifier
from sklearn.svm import SVC
from sklearn.neighbors import KNeighborsClassifier

X = df_encoded.drop('HeartDisease', axis=1)
y = df_encoded['HeartDisease']

"""# ✂️ Train-Test Split

The dataset is divided into training and testing subsets.

This allows the models to learn patterns from the training data while evaluating performance on unseen data.
"""

X_train, X_test, y_train, y_test = train_test_split(
    X, y, stratify=y, test_size=0.2, random_state=42)

"""# ⚖️ Feature Scaling

Numerical features are standardised using **StandardScaler**.

Scaling is particularly important for distance-based algorithms such as **K-Nearest Neighbors (KNN)** because it ensures that all features contribute equally during distance calculations.
"""

scaler = StandardScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)

"""# 🤖 Machine Learning Model Development

Multiple supervised classification algorithms are trained and compared to identify the best-performing model for heart disease prediction.

The implemented models include:

- Logistic Regression
- K-Nearest Neighbors (KNN)
- Naive Bayes
- Support Vector Machine (SVM)
- Decision Tree Classifier
"""

models = {
    "Logistic Regression": LogisticRegression(),
    "KNN": KNeighborsClassifier(),
    "Naive Bayes": GaussianNB(),
    "Decision Tree": DecisionTreeClassifier(),
    "SVM (RBF Kernel)": SVC(probability=True)
}

results = []

for name, model in models.items():
    model.fit(X_train_scaled, y_train)
    y_pred = model.predict(X_test_scaled)
    acc = accuracy_score(y_test, y_pred)
    f1 = f1_score(y_test, y_pred)
    results.append({
        'Model': name,
        'Accuracy': round(acc, 4),
        'F1 Score': round(f1, 4)
    })

"""# 📈 Model Evaluation

Each trained model is evaluated using standard classification metrics including:

- Accuracy
- Precision
- Recall
- F1 Score

The results are compared to determine the most suitable model for deployment.
"""

results

"""# 💾 Saving the Final Model

The best-performing model along with the fitted StandardScaler and feature column structure are saved using **Joblib**.

These files are later loaded into the Streamlit application to ensure that user inputs undergo the same preprocessing pipeline used during training.
"""

import joblib

joblib.dump(models['KNN'], 'knn_heart_model.pkl')
