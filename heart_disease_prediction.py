"""# 🔧 Data Cleaning

Certain clinical variables contain zero values that are not medically meaningful.

To improve data quality:

- Zero values in **Cholesterol** are replaced with the mean of valid observations.
- Zero values in **Resting Blood Pressure** are also replaced with the corresponding mean.

This preprocessing step ensures more realistic feature values for model training.
"""

cholesterol_mean = df.loc[df['Cholesterol'] != 0, 'Cholesterol'].mean()

# Step 2: Replace 0s with the calculated mean
df['Cholesterol'] = df['Cholesterol'].replace(0, cholesterol_mean)

# Optional: Round to 2 decimal places (if needed)
df['Cholesterol'] = df['Cholesterol'].round(2)

